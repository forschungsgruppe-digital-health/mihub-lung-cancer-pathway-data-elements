def instance_name(name: str) -> str:
    return name[0].upper() + name[1:]


def fsh_str(s) -> str:
    if s is None:
        return '""'
    s = str(s)
    if "\n" in s or len(s) > 100 or '"' in s:
        return '"""' + s.replace('"""', '\\"""') + '"""'
    return '"' + s.replace('"', '\\"') + '"'


def fsh_code(s) -> str:
    if s is None:
        return "#unknown"
    s = str(s).strip().replace("/", "-")
    return f"#{s}"


